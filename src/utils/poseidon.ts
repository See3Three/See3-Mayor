export function signEddsa(message: bigint, privateKey: bigint) {
  const public_key = mulPointEscalar(Base8, privateKey);
  const public_key_x = public_key[0];
  const public_key_y = public_key[1];

  // Deterministically generate r using the privateKey and message.
  const r = hash2([privateKey, message]);

  // Generate R using r and Base8.
  const R = mulPointEscalar(Base8, r);
  const Rx = R[0];
  const Ry = R[1];

  // Calculate the hash h using Rx, Ry (not multiplied by 8), publicKey, and message.
  const h = hash5([Rx, Ry, public_key_x, public_key_y, message]);

  // Calculate s as before.
  const s = (r + h * privateKey) % subOrder;

  // Convert to hexadecimal strings as before.
  const Rx_hex = "0x" + Rx.toString(16).padStart(64, "0");
  const Ry_hex = "0x" + Ry.toString(16).padStart(64, "0");
  const s_hex = "0x" + s.toString(16).padStart(64, "0");

  return {
    "signature-R": {
      x: Rx_hex,
      y: Ry_hex,
    },
    "signature-s": s_hex,
  };
}

export function getPublicKey(privateKey: bigint) {
  const authority_public_key_point = mulPointEscalar(
    Base8,
    privateKey,
  );
  const authority_public_key_x =
    '0x' + authority_public_key_point[0].toString(16).padStart(64, '0');
  const authority_public_key_y =
    '0x' + authority_public_key_point[1].toString(16).padStart(64, '0');
  const authority_public_key = [
    authority_public_key_x,
    authority_public_key_y,
  ] as [string, string];
  return authority_public_key;
}