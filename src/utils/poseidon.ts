function convert1DTo2D(
  arrayType: "mds" | "ark",
  t: number,
  oneDArray: bigint[]
) {
  const twoDArray: bigint[][] = [];
  let numRows: number;

  if (arrayType === "mds") {
    numRows = t;
  } else {
    numRows = oneDArray.length / t;
  }

  for (let i = 0; i < numRows; i++) {
    twoDArray[i] = [];
    for (let j = 0; j < t; j++) {
      twoDArray[i][j] = oneDArray[i * t + j];
    }
  }

  return twoDArray;
}

